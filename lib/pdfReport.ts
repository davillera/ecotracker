export async function generatePDFReport(
  userId: string,
  startDate?: string,
  endDate?: string
): Promise<string | null> {
  try {
    // Get current user
    const { data: { user } } = await supabase.auth.getUser();
    const userEmail = user?.email || 'user@email.com';
    const userName = userEmail.split('@')[0];

    // Fetch data
    let mealsQuery = supabase.from('meals').select('*').eq('user_id', userId);
    let transportQuery = supabase.from('transport').select('*').eq('user_id', userId);
    let energyQuery = supabase.from('energy_consumption').select('*').eq('user_id', userId);

    if (startDate) {
      mealsQuery = mealsQuery.gte('date', startDate);
      transportQuery = transportQuery.gte('date', startDate);
      energyQuery = energyQuery.gte('date', startDate);
    }
    if (endDate) {
      mealsQuery = mealsQuery.lte('date', endDate);
      transportQuery = transportQuery.lte('date', endDate);
      energyQuery = energyQuery.lte('date', endDate);
    }

    const [mealsResult, transportResult, energyResult, achievementsResult] = await Promise.all([
      mealsQuery,
      transportQuery,
      energyQuery,
      supabase.from('achievements').select('*').eq('user_id', userId),
    ]);

    const meals = mealsResult.data || [];
    const transport = transportResult.data || [];
    const energy = energyResult.data || [];
    const achievements = achievementsResult.data || [];

    const reportData: ReportData = {
      userName,
      email: userEmail,
      period: startDate && endDate 
        ? `${startDate} to ${endDate}` 
        : 'All Time',
      totalFootprint: 
        meals.reduce((sum, m) => sum + m.carbon_footprint, 0) +
        transport.reduce((sum, t) => sum + t.carbon_footprint, 0) +
        energy.reduce((sum, e) => sum + e.carbon_footprint, 0),
      mealsFootprint: meals.reduce((sum, m) => sum + m.carbon_footprint, 0),
      transportFootprint: transport.reduce((sum, t) => sum + t.carbon_footprint, 0),
      energyFootprint: energy.reduce((sum, e) => sum + e.carbon_footprint, 0),
      mealsCount: meals.length,
      transportCount: transport.length,
      energyCount: energy.length,
      achievements: achievements.length,
      level: 1,
    };

    const html = generateHTMLReport(reportData);
    const fileName = `ecotracker_report_${Date.now()}.html`;
    const fileUri = `${FileSystem.documentDirectory}${fileName}`;

    await FileSystem.writeAsStringAsync(fileUri, html);

    if (await Sharing.isAvailableAsync()) {
      await Sharing.shareAsync(fileUri);
    }

    return fileUri;
  } catch (error) {
    console.error('Error generating PDF report:', error);
    return null;
  }
}
